/*
P: list of string names
R: return list of my friends name
E: Input = ["Ryan", "Kieran", "Jason", "Yous"] Output = ["Ryan", "Yous"]
P: Loop through friends, filter out elements with length = 4
*/

// --- My Solution:
function friend(friends){
  return friends.filter((element)=> element.length === 4)
}
